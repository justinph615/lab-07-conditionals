

firstnum = float(input("Give me a number: "))
secondnum = float(input("Give me another number: "))
operation = input("What operation do you want? (mul/div/mod) ")


if operation == "mul"or operation == "MUL":
		
			print(firstnum * secondnum)


elif operation == "div" or operation == "DIV":


		print(firstnum / secondnum)


		 
elif operation == "mod" or operation == "MOD":


		
		print(firstnum % secondnum)


else: 
		print("*** invalid operation")


		



	